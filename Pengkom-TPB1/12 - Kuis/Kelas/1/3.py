# Deskripsi : program mencari volume dari bola sesuai jari-jari yang dimiliki bola tersebut

# KAMUS
# r : int

# Melakukan input jari-jari bola, dengan r > 0
r = int(input('Masukkan jari-jari: '))

# Mendefinisikan volume bola sesuai rumus yang berlaku
V = (4*3.14*(r**3))/3

# Menampilkan V
print(f'Maka volume dari bola adalah {V} satuan volume')

